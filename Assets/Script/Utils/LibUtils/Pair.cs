using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

public class Pair<TLeft, TRight>
{
    private TLeft m_left;
	private TRight m_right;

	public Pair() { }
	public Pair(TLeft l, TRight r)
	{
		m_left = l;
		m_right = r;
	}

    public TLeft left
    {
        get
        {
            return m_left;
        }
        set
        {
            m_left = value;
        }
    }
    public TRight right
    {
        get
        {
            return m_right;
        }
        set
        {
            m_right = value;
        }
    }
}
